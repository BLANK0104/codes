
# Create a sample vector
sample_vector <- c(2, 4, 6, 8, 10)

# Find and display the sum of the vector's elements
sum_result <- sum(sample_vector)
print(paste("Sum:", sum_result))

# Find and display the mean of the vector's elements
mean_result <- mean(sample_vector)
print(paste("Mean:", mean_result))

# Find and display the product of the vector's elements
product_result <- prod(sample_vector)
print(paste("Product:", product_result))
